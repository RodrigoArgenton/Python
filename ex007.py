#variavel para receber o valor para calcular a média entre duas notas.
nota1 = float(input('Qual seria a primeira nota? '))

#variavel para receber o valor para calcular a média entre duas notas.
nota2 = float(input('Qual seria a segunda nota? '))

#variavel com o objetivo de somar dois valores e dividir o mesmo por 2.
media = (nota1 + nota2) / 2

#mostrando o valor da divisão
print(f'A média entre {nota1} e {nota2} é: {media}.')
